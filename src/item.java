
public class item {
    private int itemId;
    private String itemName;
    private boolean unitPrice;
    private int quantity;
    private boolean qtyAmount;
	public int getItemId() {
		return itemId;
	}
	public void setItemId(int itemId) {
		this.itemId = itemId;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public boolean isUnitPrice() {
		return unitPrice;
	}
	public void setUnitPrice(boolean unitPrice) {
		this.unitPrice = unitPrice;
	}
	public int getQuantity() {
		return quantity;
	}
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	public boolean isQtyAmount() {
		return qtyAmount;
	}
	public void setQtyAmount(boolean qtyAmount) {
		this.qtyAmount = qtyAmount;
	}
    

    
}
